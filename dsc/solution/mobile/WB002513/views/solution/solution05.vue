            <h2 class="subtitle mt-1">二、解決方案</h2>
            <p>要在各種不同條件範圍內找出客戶所需，且快又有效正確， PICKING 作業就很重要，所以ERP系統中必需要設計可以記錄產品類型屬性的資訊，並且在接訂單時就可帶出預設值，並由業務依本次客戶所需做資料調整，如此可加快單據維護速度，也將資訊完整記錄於系統中。接續出貨作業時，也可由備貨人員直接點選出本次預交貨的訂單，這些屬性資料也隨之帶入，此時就是作業重點了，ERP系統必需要能自動將該產品類型各屬性的庫存資訊依序帶出，以便於備貨人員點選後記錄於系統中，如此方完成出貨作業。這樣的控管從接單到出貨整體一致，不會出錯也可快速完成作業程序，對於企業效率提升有很大的助益。</p>
