            <h2 class="subtitle">一、太陽能產業目前所面臨的重要挑戰</h2>
            <p>太陽能電池產業第一個會面臨的問題就是接訂單時，客戶只能指定型號，無法指定確定的產品料號或特定屬性，也就是說客戶下單時只能提供所需產品的屬性範圍，例如效率、阻值、厚度..等是多少範圍，但是這樣的記錄模式對於出貨作業就有很大的困擾，要如何在各種不同條件範圍內找出客戶所需，且快又有效正確，對於備貨出貨單位是一個很重要的挑戰。</p>
